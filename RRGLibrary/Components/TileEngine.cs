        public TileEngine(string filename)
        {
            
            this.filename = filename;
            tiles[0] = new Rectangle(0, 0, 128, 128);
            tiles[1] = new Rectangle(128, 0, 128, 128);
            tiles[2] = new Rectangle(0, 128, 128, 128);
            tiles[3] = new Rectangle(128, 128, 128, 128);
        }

        protected override void Load()
        {
            spriteBatch = Parent.Engine.SpriteBatch;
            tileset = Parent.Engine.Content.Load<Texture2D>(filename);
            base.Load();
        }

        public override void Draw()
        {
            base.Draw();
            int mapWidth = map.GetLength(1);
            int mapHeight = map.GetLength(0);
            for (int x = 0; x < mapWidth; x++)
            {
                for (int y = 0; y < mapHeight; y++)
                {
                    spriteBatch.Draw(tileset,
                    new Rectangle(x * tilewidth,
                    y * tileheight,
                    tilewidth,
                    tileheight),
                    tiles[map[y, x]],
                    Color.White);
                }
            }
        }

 
        public virtual void Pause()
        {
            Enabled = !Enabled;
            
        }
